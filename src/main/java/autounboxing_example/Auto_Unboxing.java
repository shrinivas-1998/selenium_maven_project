package autounboxing_example;

public class Auto_Unboxing 
{
	 public static void main(String[] args) 
	 {
	        // Creating a Double object
	        Double obj1 = 12.34;
	        // Auto-unboxing: converting the Double object to a primitive double
	        double d = obj1;
	        // Printing the unboxed double value
	        System.out.println("Unboxed value: " + d);
	    }

}
